package com.telenav.telephony;

/**
 * This class provides access to fundamental information about the host device.
 * 
 *@author fqming (fqming@telenav.cn)
 *@date Jul 11, 2010
 */
public class TnDeviceInfo
{
    /**
     * rim platform.
     */
    public final static int PLATFORM_RIM = 0;
    
    /**
     * android platform.
     */
    public final static int PLATFORM_ANDROID = 1;
    
    /**
     * j2me platform.
     */
    public final static int PLATFORM_J2ME = 2;
    
    /**
     * j2se platform.
     */
    public final static int PLATFORM_J2SE = 3;
    
    /**
     * Retrieves the platform.
     */
    public int platform;
    
    /**
     * Retrieves the PIN number for this device.
     */
    public String deviceId;
    
    /**
     * Retrieves the current device's product name.
     */
    public String deviceName;
    
    /**
     * Retrieve the device manufacturer's name.
     */
    public String manufacturerName;
    
    /**
     * Retrieves the platform version.
     */
    public String platformVersion;
    
    /**
     * Retrieves the device's software version.
     */
    public String softwareVersion;
    
    /**
     * Determines if this device is really a simulator. 
     */
    public boolean isSimulator;
    
    /**
     * Retrieves this device system locale name. 
     */
    public String systemLocale;
    
    /**
     * Retrieves this device release version. 
     */
    public String releaseVersion;
    
    
}
