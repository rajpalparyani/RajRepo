package com.telenav.location;

/**
 * A TnLocationException indicates an unexpected error condition in a method.
 * 
 *@author fqming (fqming@telenav.cn)
 *@date Aug 2, 2010
 */
public class TnLocationException extends Exception
{

    /**
     * serial version UID
     */
    private static final long serialVersionUID = 3647580375525379791L;

    /**
     * Constructs a LocationException with the specified detail message. The error message string s can later be retrieved
     * by the Throwable.getMessage() method of class java.lang.Throwable.
     * 
     * @param msg the detail message.
     */
    public TnLocationException(String msg)
    {
        super(msg);
    }
}
