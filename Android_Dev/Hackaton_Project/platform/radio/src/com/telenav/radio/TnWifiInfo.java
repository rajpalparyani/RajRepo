package com.telenav.radio;

/**
 * Encapsulates information for a wifi access point. 
 * 
 *@author fqming (fqming@telenav.cn)
 *@date Jul 11, 2010
 */
public class TnWifiInfo
{
    /**
     * Retrieves the current BSSID.
     */
    public String bssid;
    
    /**
     * Retrieves the current data rate in Mbps.
     */
    public int speed;
    
    /**
     * Retrieves the current signal level in dBm.
     */
    public int signalLevel;
    
    /**
     * Retrieves the current SSID.
     */
    public String ssid;
}
