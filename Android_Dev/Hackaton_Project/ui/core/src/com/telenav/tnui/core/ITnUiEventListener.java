package com.telenav.tnui.core;

/**
 * Interface definition for a callback to be invoked when a ui event is dispatched to this component. 
 * The callback will be invoked before the ui event is given to the component. 
 * <br />
 * The event will include command event, key event, touch event, trackball event and ui inner event etc.
 * Please see {@link TnUiEvent} for more details.
 * 
 *@author fqming (fqming@telenav.cn)
 *@date 2010-6-22
 */
public interface ITnUiEventListener
{
    /**
     * Called when the ui event has occurred.
     * 
     * @param tnUiEvent {@link TnUiEvent} - The ui event. 
     * @return isHandle
     */
    public boolean handleUiEvent(TnUiEvent tnUiEvent);
}
