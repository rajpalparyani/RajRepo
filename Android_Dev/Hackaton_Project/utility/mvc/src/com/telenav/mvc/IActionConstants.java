package com.telenav.mvc;

/**
 * Action constants definition.<br>
 * All action id must start with "ACTION_"<br>
 * 
 * @author zhdong
 * 
 */
public interface IActionConstants
{
    /**
     * The base action id
     */
    public static final int ACTION_BASE = 4000000;
    
    /**
     * Action none which means no action at all.
     */
    public static final int ACTION_NONE = -1;
    
    /**
     * All common action must be based on this value.
     */
    public static final int ACTION_COMMON_BASE = ACTION_BASE + 1000;

    /**
     * All user defined action must be based on this value.
     */
    public static final int ACTION_USER_BASE = ACTION_BASE + 2000;
}
