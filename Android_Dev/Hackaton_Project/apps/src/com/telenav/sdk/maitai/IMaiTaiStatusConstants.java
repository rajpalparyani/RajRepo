public interface IMaiTaiStatusConstants
{
    //value from 1000 - 1999 presents Informational message
    
    
    //value from 2000 - 2999 presents successful value
    public static final int STATUS_SUCCESS               = 2000;
    
    //value from 3000 - 3999 presents:
    //Redirection - The request requires further action by the requestor to complete the request.
    
    
    //value from 4000 - 4999 presents:
    //MaiTai Client Error - The request was unsuccessful due to a client side error
    public static final int STATUS_BAD_REQUEST           = 4000;
    public static final int STATUS_UNAUTHORIZED          = 4010;
    public static final int STATUS_LACK_OF_ENTITLEMENT   = 4030;
    public static final int STATUS_TOO_MANY_REQUESTS     = 4031;
    public static final int STATUS_ACCOUNT_SUSPENDED     = 4032;
    public static final int STATUS_APPLICATION_NOT_FOUND = 4040;
    
    //value from 5000 - 5999 presents:
    //MaiTai Server Error - The server encountered an unexpected condition that prevented it from fulfilling the request.
    public static final int STATUS_NOT_IMPLEMENTED       = 5010;
    public static final int STATUS_VERSION_UNSUPPORTED   = 5050;
    
    //value from 6000 - 6999 presents:
    //MaiTai Application Error - 
    //  The server has processed the request and has found an application logic error that prevented it from correctly completing the request.
    public static final int STATUS_PROCESS_ERROR         = 6100;
}
