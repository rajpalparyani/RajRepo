package com.telenav.module.ac.favorite;

import com.telenav.module.AppConfigHelper;
import com.telenav.mvc.AbstractCommonUiDecorator;
import com.telenav.tnui.core.AbstractTnUiHelper;
import com.telenav.tnui.core.TnUiArgs.TnUiArgAdapter;
import com.telenav.ui.citizen.android.AndroidCitizenUiHelper;
import com.telenav.util.PrimitiveTypeCache;

/**
 * @author wzhu (wzhu@telenav.cn)
 * @date 2010-8-26
 */
class FavoriteUiDecorator extends AbstractCommonUiDecorator
{
    private final static int ID_LABEL_TITLE_WIDTH = 1;

    private final static int ID_BUTTON_TITLE_WIDTH = 2;

    private final static int ID_NULLFIELD_TITLE_WIDTH = 3;

    private final static int ID_TITLE_CONTAINER_HEIGHT = 4;

    private final static int ID_TEXTFIELD_WIDTH = 5;

    private final static int ID_LABEL_TITLE_HEIGHT = 6;

    private final static int ID_ADDRESSLISTITEM_HEIGHT = 7;

    private final static int ID_BUTTON_EDIT_FAVORIVITE_WIDTH = 8;

    private final static int ID_LABEL_SUB_CATEGORY_WIDTH = 9;
    
    private final static int ID_LISTITEM_WIDTH = 10;
    
    private final static int ID_TEXTFIELD_CONTAINER_HEIGHT = 11;
    
    private final static int ID_TEXTFIELD_HEIGHT = 12;
    
    private final static int ID_TOP_BUTTON_CONTAINER_WIDTH = 13;
    
    private final static int ID_LEFT_TITLE_CONTAINER_WIDTH = 14;
    
    private final static int ID_RIGHT_TITLE_CONTAINER_WIDTH = 15;
    
    private final static int ID_CIRCLE_RADIUS = 16;
    
    private final static int ID_ANIMATION_CONTAINER_WIDTH = 17;
    
    private final static int ID_LIST_HEIGHT = 18;
    
    private final static int ID_BOTTOM_CONTAINER_HEIGHT = 19;
    
    private final static int ID_BOTTOM_NULLFIELD_WIDTH = 20;
    
    private final static int ID_EDIT_MODE_BUTTON_WIDTH = 21;
    
    private final static int ID_MULTILINE_WIDTH = 22;
    
    private final static int ID_MULTILINE_HEIGHT = 23;
    
    private final static int ID_NULLFIELD_HEIGHT = 24;
    
    private final static int ID_BUTON_HEIGHT = 25;
    
    private final static int ID_BUTTON_CONTAINER_HEIGHT = 26;
    
    private final static int ID_BUTTON_EDIT_CATEGORY_WIDTH = 27;
    
    private final static int ID_NULLFIELD_EDIT_CATEGORY_WIDTH = 28;
    
    private final static int ID_POPUP_TITLE_HEIGHT = 29;
    
    private final static int ID_TOP_BUTTON_HEIGHT = 30;
    
    private final static int ID_FAVORITE_EMPTY_PADDING_TOP = 31;
    
    private final static int SCALE = 1000;

    public TnUiArgAdapter TITLE_LABEL_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_LABEL_TITLE_WIDTH), this);

    public TnUiArgAdapter TITLE_BUTTON_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_BUTTON_TITLE_WIDTH), this);

    public TnUiArgAdapter TITLE_NULLFIELD_TITLE_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_NULLFIELD_TITLE_WIDTH), this);

    public TnUiArgAdapter TITLE_CONTAINER_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_TITLE_CONTAINER_HEIGHT), this);

    public TnUiArgAdapter TEXTFIELD_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_TEXTFIELD_WIDTH), this);

    public TnUiArgAdapter LABEL_TITLE_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_LABEL_TITLE_HEIGHT), this);

    public TnUiArgAdapter ADDRESSLISTITEM_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_ADDRESSLISTITEM_HEIGHT), this);

    public TnUiArgAdapter BUTTON_EDIT_FAVORIVITE_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_BUTTON_EDIT_FAVORIVITE_WIDTH), this);

    public TnUiArgAdapter LABEL_SUB_CATEGORY_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_LABEL_SUB_CATEGORY_WIDTH), this);
    
    public TnUiArgAdapter LISTITEM_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_LISTITEM_WIDTH), this);
    
    public TnUiArgAdapter TEXTFIELD_CONTAINER_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_TEXTFIELD_CONTAINER_HEIGHT), this);
    
    public TnUiArgAdapter TEXTFIELD_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_TEXTFIELD_HEIGHT), this);
    
    public TnUiArgAdapter TOP_BUTTON_CONTAINER_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_TOP_BUTTON_CONTAINER_WIDTH), this);
    
    public TnUiArgAdapter LEFT_TITLE_CONTAINER_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_LEFT_TITLE_CONTAINER_WIDTH), this);
    
    public TnUiArgAdapter RIGHT_TITLE_CONTAINER_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_RIGHT_TITLE_CONTAINER_WIDTH), this);
    
    public TnUiArgAdapter CIRCLE_RADIUS = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_CIRCLE_RADIUS), this);
    
    public TnUiArgAdapter ANIMATION_CONTAINER_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_ANIMATION_CONTAINER_WIDTH), this);
    
    public TnUiArgAdapter LIST_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_LIST_HEIGHT), this);
    
    public TnUiArgAdapter BOTTOM_CONTAINER_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_BOTTOM_CONTAINER_HEIGHT), this);
    
    public TnUiArgAdapter BOTTOM_NULLFIELD_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_BOTTOM_NULLFIELD_WIDTH), this);
    
    public TnUiArgAdapter EDIT_MODE_BUTTON_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_EDIT_MODE_BUTTON_WIDTH), this);
    

    public TnUiArgAdapter MULTILINE_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_MULTILINE_WIDTH), this);
    
    public TnUiArgAdapter MULTILINE_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_MULTILINE_HEIGHT), this);
    
    public TnUiArgAdapter NULLFIELD_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_NULLFIELD_HEIGHT), this);
    
    public TnUiArgAdapter BUTON_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_BUTON_HEIGHT), this);
    
    public TnUiArgAdapter BUTTON_CONTAINER_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_BUTTON_CONTAINER_HEIGHT), this);
    
    public TnUiArgAdapter BUTTON_EDIT_CATEGORY_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_BUTTON_EDIT_CATEGORY_WIDTH), this);
    
    public TnUiArgAdapter NULLFIELD_EDIT_CATEGORY_WIDTH = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_NULLFIELD_EDIT_CATEGORY_WIDTH), this);
    
    public TnUiArgAdapter POPUP_TITLE_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_POPUP_TITLE_HEIGHT), this);
    
    public TnUiArgAdapter TOP_BUTTON_HEIGHT = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_TOP_BUTTON_HEIGHT), this);
    
    public TnUiArgAdapter FAVORITE_EMPTY_PADDING_TOP = new TnUiArgAdapter(PrimitiveTypeCache.valueOf(ID_FAVORITE_EMPTY_PADDING_TOP), this);
    
    protected Object decorateDelegate(TnUiArgAdapter args)
    {
        int key = ((Integer) args.getKey()).intValue();
        switch (key)
        {
            case ID_LABEL_TITLE_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 750 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 760 / SCALE);
            }
            case ID_BUTTON_TITLE_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 120 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 80 / SCALE);
            }
            case ID_NULLFIELD_TITLE_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 30 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 20 / SCALE);
            }
            case ID_TITLE_CONTAINER_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                {
                    //unify the title height with native xml screen title height
                    return PrimitiveTypeCache.valueOf(AndroidCitizenUiHelper.getPixelsByDip(53)); 
                }
                else
                {
                    if (AppConfigHelper.isTabletSize())
                    {
                        return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 938 / 10000);
                    }
                    else
                    {
                        return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 127 / SCALE);
                    }
                }
            }
            case ID_TEXTFIELD_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 938 / SCALE);
                else
                {
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 958 / SCALE);
                }
            }
            case ID_LABEL_TITLE_HEIGHT:
            {
                //unify the title height with native xml screen title height
                return PrimitiveTypeCache.valueOf(AndroidCitizenUiHelper.getPixelsByDip(53)); 
            }
            case ID_ADDRESSLISTITEM_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 100 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 150 / SCALE);
            }
            case ID_BUTTON_EDIT_FAVORIVITE_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 100 / SCALE);
                else
                {
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 70 / SCALE);
                }
            }
            case ID_LABEL_SUB_CATEGORY_WIDTH: 
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 770 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 850 / SCALE);
            }
            case ID_LISTITEM_WIDTH:
            {
                return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth());
            }
            case ID_TEXTFIELD_CONTAINER_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 85 / SCALE);
                else
                {
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 127 / SCALE);
                }
            }
            case ID_TEXTFIELD_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                {
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getMaxDisplaySize() * 62 / 1000);
                }
                else
                {
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getMaxDisplaySize() * 55 / 1000);
                }
            }
            case ID_TOP_BUTTON_CONTAINER_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 200 / SCALE);
                else
                {
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 200 / SCALE);
                }
            }
            case ID_LEFT_TITLE_CONTAINER_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 850 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 880 / SCALE);
            }
            case ID_RIGHT_TITLE_CONTAINER_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 130 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 100 / SCALE);
            }
            case ID_CIRCLE_RADIUS:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 70 / SCALE);
                else
                {
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 50 / SCALE);
                }
            }
            case ID_ANIMATION_CONTAINER_WIDTH:
            {
                return PrimitiveTypeCache.valueOf(AppConfigHelper.getMaxDisplaySize() * 65 / 1000);
            }
            case ID_LIST_HEIGHT:
            {
                int listHeight = AppConfigHelper.getDisplayHeight()
                        - BOTTOM_CONTAINER_HEIGHT.getInt()
                        - TITLE_CONTAINER_HEIGHT.getInt()
                        - TEXTFIELD_CONTAINER_HEIGHT.getInt();
                
                return PrimitiveTypeCache.valueOf(listHeight);
            }
            case ID_BOTTOM_CONTAINER_HEIGHT:
            {
                return PrimitiveTypeCache.valueOf(AppConfigHelper.getMaxDisplaySize() / 10);
            }
            case ID_BOTTOM_NULLFIELD_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 30 / SCALE);
                else
                {
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 20 / SCALE);
                }
            }
            case ID_EDIT_MODE_BUTTON_WIDTH:
            {
                return PrimitiveTypeCache.valueOf(AppConfigHelper.getMinDisplaySize() * 180 / SCALE);
            }
            case ID_MULTILINE_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 650 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 700/ SCALE);
            }
            case ID_MULTILINE_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 100 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 100 / SCALE);
            }
            case ID_NULLFIELD_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 20 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 30 / SCALE);
            }
            case ID_BUTON_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 65 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 105 / SCALE);
            }
            case ID_BUTTON_CONTAINER_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 130 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 190 / SCALE);
            }
            case ID_BUTTON_EDIT_CATEGORY_WIDTH:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 320 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 320 / SCALE);
            }
            case ID_NULLFIELD_EDIT_CATEGORY_WIDTH:
            {
                return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 50 / SCALE);
            }
            case ID_POPUP_TITLE_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 70 / SCALE);
                else
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 91 / SCALE);
            }
            case ID_FAVORITE_EMPTY_PADDING_TOP:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 250/1280);
                else
                {
                    return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 30/ 720);
                }
            }
            case ID_TOP_BUTTON_HEIGHT:
            {
                if (((AbstractTnUiHelper) AbstractTnUiHelper.getInstance()).getOrientation() == AbstractTnUiHelper.ORIENTATION_PORTRAIT)
                {
                    if (AppConfigHelper.isTabletSize())
                    {
                        return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 50 / 1000);
                    }      
                    else
                    {
                        return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 65 / 1000);
                    }
                }
                else
                {
                    if (AppConfigHelper.isTabletSize())
                    {
                        return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayWidth() * 50 / 1000);
                    }
                    else
                    {
                        return PrimitiveTypeCache.valueOf(AppConfigHelper.getDisplayHeight() * 100 / SCALE);
                    }
                }
            }
        }
        return null;
    }

}
