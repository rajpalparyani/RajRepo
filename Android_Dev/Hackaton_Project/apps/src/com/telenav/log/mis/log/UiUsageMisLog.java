public class UiUsageMisLog extends AbstractMisLog
{

    public UiUsageMisLog(String id, int priority)
    {
        super(id, TYPE_UI_USAGE_REPORT, priority);
    }

    public void setUiId(String uiId)
    {
        this.setAttribute(ATTR_UI_ID, uiId);
    }

    public void setUiContainer(String uiContainer)
    {
        this.setAttribute(ATTR_UI_CONTAINER, uiContainer);
    }

    public void setUiUseCase(String uiUseCase)
    {
        this.setAttribute(ATTR_UI_USE_CASE, uiUseCase);
    }

}
