    public byte[] synthesize(AudioData[] audios)
    {
        int size = 0; // total size of all audios
        for (int i = 0; i < audios.length; i++)
        {
            try
            {
                if (audios[i].getData() != null)
                {
                    byte[] dataBuff = audios[i].getData();
                    if (i == 0) // keep the first file's header
                    {
                        size += dataBuff.length - getFooterSize(dataBuff);
                    }
                    else
                    {
                        size += dataBuff.length - getHeaderSize(dataBuff) - getFooterSize(dataBuff);
                    }
                }
            }
            catch (Throwable t)
            {
                Logger.log(SynthesizerMp3.class.getName(), t);
            }
        }
        if (size <= 0)
            return null;

        //Remove silenceData to fix bug http://jira.telenav.com:8080/browse/TNANDROID-4182, and test on moto atrix, cannot reproduce with http://jira.telenav.com:8080/browse/TN-1161
//        int silenceHeader = getHeaderSize(silenceData);
//        int silenceFooter = getFooterSize(silenceData);
//        int silenceSize = silenceData.length - silenceHeader - silenceFooter;
//        size += silenceSize;
        
        byte[] buff = new byte[size];
        int index = 0;
        for (int i = 0; i < audios.length; i++)
        {
            if (audios[i].getData() != null)
            {
                byte[] data = audios[i].getData();
                int footerSize = getFooterSize(data);
                if (i == 0)
                {
                    // keep the first file's header
                    int realSize = data.length - footerSize;
                    System.arraycopy(data, 0, buff, index, realSize);
                    index += realSize;
                }
                else
                {
                    int headerSize = getHeaderSize(data);
                    int realSize = data.length - headerSize - footerSize;
                    System.arraycopy(data, headerSize, buff, index, realSize);
                    index += realSize;
                }
            }
        }
        
//        System.arraycopy(silenceData, silenceHeader, buff, index, silenceSize);
        
        
        return buff;
    }

    private int getHeaderSize(byte[] buff)
    {
        if (buff.length < 10)
            return 0;

        int size = 0;
        if (buff[0] == 'I' && buff[1] == 'D' && buff[2] == '3')
        {
            size = 10;
            boolean hasFooter = bitSet(buff[5], 4);
            size += convertToSynchsafeInt(buff, 6);

            if (hasFooter)
                size += 10;

        }
        return size;
    }

    private int getFooterSize(byte[] buff)
    {
        if (buff.length < 128)
            return 0;

        int index = buff.length - 128;
        if (buff[index] == 'T' && buff[index + 1] == 'A' && buff[index + 2] == 'G')
        {
            return 128;
        }

        return 0;
    }

    /**
     * Converts the byte array to a synchsafe integer as specified in section 6.2 of the id3v2 specification. Works
     * exactly same as the covnertToInt method except that it only calculates using the first 7 bits of each byte.
     * 
     * @param b the array of bytes
     * @return the converted integer in decimal
     */
    private int convertToSynchsafeInt(byte[] b, int index)
    {
        int value = 0;
        for (int i = index; i < index + 4; i++)
            value += (b[i] & 0x7F) << ((3 + index - i) * 7);

        return value;

    }

    private boolean bitSet(byte b, int pos)
    {
        boolean retval = false;

        if ((pos >= 0) && (pos < 8))
        {
            retval = ((b & (byte) (1 << pos)) != 0);
        }

        return retval;
    }

