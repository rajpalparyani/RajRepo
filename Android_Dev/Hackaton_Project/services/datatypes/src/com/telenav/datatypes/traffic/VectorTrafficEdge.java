package com.telenav.datatypes.traffic;

import com.telenav.datatypes.map.AbstractVectorEdge;

public class VectorTrafficEdge extends AbstractVectorEdge
{
    protected byte directionality ;
    protected String tmcId;
    protected short speedLimit;
    
    protected short trafficSpeed;
    
    protected int[][] boundingBox;
    
    protected VectorTrafficEdge()
    {
        
    }
    
    public byte getDirectionality()
    {
        return directionality;
    }

    public short getSpeedLimit()
    {
        return speedLimit;
    }

    public String getTmcId()
    {
        return tmcId;
    }

    public short getTrafficSpeed()
    {
        return trafficSpeed;
    }

    public void setDirectionality(byte d)
    {
        this.directionality = d;
    }

    public void setSpeedLimit(short speedLimit)
    {
        this.speedLimit = speedLimit;
    }

    public void setTmcId(String tmcId)
    {
        this.tmcId = tmcId;
    }

    public void setTrafficSpeed(short trafficSpeed)
    {
        this.trafficSpeed = trafficSpeed;
    }

    public void setBoundingBox(int[][] boundingBox)
    {
        this.boundingBox = boundingBox;
    }

    public int[][] getBoundingBox() 
    {
        return this.boundingBox;
    }
}
